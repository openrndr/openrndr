class TestPath {
    @Test
    fun `calling sub on a path3D should not throw exceptions`() {
        val c = path3D {
            moveTo(0.0, 0.0, 0.0)
            lineTo(100.0, 100.0, 100.0)
        }

        val fullLength = c.length
        val s = c.sub(0.0, 0.01)
        println()
        println("path3D sub length    = ${s.length}")
        println("path3D length * 0.01 = ${fullLength * 0.01}")
    }

    @Test
    fun `calling sampleEquidistant on a path3D should not throw exceptions`() {
        val c = path3D {
            moveTo(0.0, 0.0, 0.0)
            lineTo(100.0, 100.0, 100.0)
            lineTo(200.0, 200.0, 200.0)
            lineTo(300.0, 300.0, 300.0)
            lineTo(400.0, 400.0, 400.0)
        }

        val fullLength = c.length
        val s = c.sampleEquidistant(600).sub(0.0, 0.01)
        println()
        println("path3D full length   = $fullLength")
        println("path3D sub length    = ${s.length}")
        println("path3D length * 0.01 = ${fullLength * 0.01}")
    }

